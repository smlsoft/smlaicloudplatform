type IPayHttp interface{}

type PayHttp struct {
	ms  *microservice.Microservice
	cfg config.IConfig
	svc services.IPayHttpService
}

func NewPayHttp(ms *microservice.Microservice, cfg config.IConfig) PayHttp {
	pst := ms.MongoPersister(cfg.MongoPersisterConfig())
	producer := ms.Producer(cfg.MQConfig())
	cache := ms.Cacher(cfg.CacherConfig())

	repo := repositories.NewPayRepository(pst)
	repoMq := repositories.NewPaidMessageQueueRepository(producer)

	transRepo := trancache.NewCacheRepository(cache)
	masterSyncCacheRepo := mastersync.NewMasterSyncCacheRepository(cache)
	svc := services.NewPayHttpService(repo, repoMq, transRepo, masterSyncCacheRepo)

	return PayHttp{
		ms:  ms,
		cfg: cfg,
		svc: svc,
	}
}

func (h PayHttp) RegisterHttp() {

	h.ms.POST("/transaction/pay/bulk", h.SaveBulk)

	h.ms.GET("/transaction/pay", h.SearchPayPage)
	h.ms.GET("/transaction/pay/list", h.SearchPayStep)
	h.ms.POST("/transaction/pay", h.CreatePay)
	h.ms.GET("/transaction/pay/:id", h.InfoPay)
	h.ms.GET("/transaction/pay/code/:code", h.InfoPayByCode)
	h.ms.PUT("/transaction/pay/:id", h.UpdatePay)
	h.ms.DELETE("/transaction/pay/:id", h.DeletePay)
	h.ms.DELETE("/transaction/pay", h.DeletePayByGUIDs)
}

// Create Pay godoc
// @Description Create Pay
// @Tags		Pay
// @Param		Pay  body      models.Pay  true  "Pay"
// @Accept 		json
// @Success		201	{object}	common.ResponseSuccessWithID
// @Failure		401 {object}	common.AuthResponseFailed
// @Security     AccessToken
// @Router /transaction/pay [post]
func (h PayHttp) CreatePay(ctx microservice.IContext) error {
	authUsername := ctx.UserInfo().Username
	shopID := ctx.UserInfo().ShopID
	input := ctx.ReadInput()

	docReq := &models.Pay{}
	err := json.Unmarshal([]byte(input), &docReq)

	if err != nil {
		ctx.ResponseError(400, err.Error())
		return err
	}

	if err = ctx.Validate(docReq); err != nil {
		ctx.ResponseError(400, err.Error())
		return err
	}

	idx, docNo, err := h.svc.CreatePay(shopID, authUsername, *docReq)

	if err != nil {
		ctx.ResponseError(http.StatusBadRequest, err.Error())
		return err
	}

	ctx.Response(http.StatusCreated, common.ApiResponse{
		Success: true,
		ID:      idx,
		Data:    docNo,
	})
	return nil
}

