type IPaidHttp interface{}

type PaidHttp struct {
	ms  *microservice.Microservice
	cfg config.IConfig
	svc services.IPaidHttpService
}

func NewPaidHttp(ms *microservice.Microservice, cfg config.IConfig) PaidHttp {
	pst := ms.MongoPersister(cfg.MongoPersisterConfig())
	cache := ms.Cacher(cfg.CacherConfig())

	repo := repositories.NewPaidRepository(pst)

	transRepo := trancache.NewCacheRepository(cache)
	masterSyncCacheRepo := mastersync.NewMasterSyncCacheRepository(cache)
	svc := services.NewPaidHttpService(repo, transRepo, masterSyncCacheRepo)

	return PaidHttp{
		ms:  ms,
		cfg: cfg,
		svc: svc,
	}
}

func (h PaidHttp) RegisterHttp() {

	h.ms.POST("/transaction/paid/bulk", h.SaveBulk)

	h.ms.GET("/transaction/paid", h.SearchPaidPage)
	h.ms.GET("/transaction/paid/list", h.SearchPaidStep)
	h.ms.POST("/transaction/paid", h.CreatePaid)
	h.ms.GET("/transaction/paid/:id", h.InfoPaid)
	h.ms.GET("/transaction/paid/code/:code", h.InfoPaidByCode)
	h.ms.PUT("/transaction/paid/:id", h.UpdatePaid)
	h.ms.DELETE("/transaction/paid/:id", h.DeletePaid)
	h.ms.DELETE("/transaction/paid", h.DeletePaidByGUIDs)
}

// Create Paid godoc
// @Description Create Paid
// @Tags		Paid
// @Param		Paid  body      models.Paid  true  "Paid"
// @Accept 		json
// @Success		201	{object}	common.ResponseSuccessWithID
// @Failure		401 {object}	common.AuthResponseFailed
// @Security     AccessToken
// @Router /transaction/paid [post]
func (h PaidHttp) CreatePaid(ctx microservice.IContext) error {
	authUsername := ctx.UserInfo().Username
	shopID := ctx.UserInfo().ShopID
	input := ctx.ReadInput()

	docReq := &models.Paid{}
	err := json.Unmarshal([]byte(input), &docReq)

	if err != nil {
		ctx.ResponseError(400, err.Error())
		return err
	}

	if err = ctx.Validate(docReq); err != nil {
		ctx.ResponseError(400, err.Error())
		return err
	}

	idx, docNo, err := h.svc.CreatePaid(shopID, authUsername, *docReq)

	if err != nil {
		ctx.ResponseError(http.StatusBadRequest, err.Error())
		return err
	}

	ctx.Response(http.StatusCreated, common.ApiResponse{
		Success: true,
		ID:      idx,
		Data:    docNo,
	})
	return nil
}

