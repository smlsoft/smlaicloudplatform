type IMediaHttp interface{}

type MediaHttp struct {
	ms  *microservice.Microservice
	cfg config.IConfig
	svc services.IMediaHttpService
}

func NewMediaHttp(ms *microservice.Microservice, cfg config.IConfig) MediaHttp {
	pst := ms.MongoPersister(cfg.MongoPersisterConfig())
	cache := ms.Cacher(cfg.CacherConfig())

	repo := repositories.NewMediaRepository(pst)

	masterSyncCacheRepo := mastersync.NewMasterSyncCacheRepository(cache)
	svc := services.NewMediaHttpService(repo, masterSyncCacheRepo, 15*time.Second)

	return MediaHttp{
		ms:  ms,
		cfg: cfg,
		svc: svc,
	}
}

func (h MediaHttp) RegisterHttp() {

	h.ms.POST("/pos/media/bulk", h.SaveBulk)

	h.ms.GET("/pos/media", h.SearchMediaPage)
	h.ms.GET("/pos/media/list", h.SearchMediaStep)
	h.ms.POST("/pos/media", h.CreateMedia)
	h.ms.GET("/pos/media/:id", h.InfoMedia)
	h.ms.GET("/pos/media/code/:code", h.InfoMediaByCode)
	h.ms.PUT("/pos/media/:id", h.UpdateMedia)
	h.ms.DELETE("/pos/media/:id", h.DeleteMedia)
	h.ms.DELETE("/pos/media", h.DeleteMediaByGUIDs)
}

// Create Media godoc
// @Description Create Media
// @Tags		Media
// @Param		Media  body      models.Media  true  "Media"
// @Accept 		json
// @Success		201	{object}	common.ResponseSuccessWithID
// @Failure		401 {object}	common.AuthResponseFailed
// @Security     AccessToken
// @Router /pos/media [post]
func (h MediaHttp) CreateMedia(ctx microservice.IContext) error {
	authUsername := ctx.UserInfo().Username
	shopID := ctx.UserInfo().ShopID
	input := ctx.ReadInput()

	docReq := &models.Media{}
	err := json.Unmarshal([]byte(input), &docReq)

	if err != nil {
		ctx.ResponseError(400, err.Error())
		return err
	}

	if err = ctx.Validate(docReq); err != nil {
		ctx.ResponseError(400, err.Error())
		return err
	}

	idx, err := h.svc.CreateMedia(shopID, authUsername, *docReq)

	if err != nil {
		ctx.ResponseError(http.StatusBadRequest, err.Error())
		return err
	}

	ctx.Response(http.StatusCreated, common.ApiResponse{
		Success: true,
		ID:      idx,
	})
	return nil
}

